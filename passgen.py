#!/usr/bin/env python3

import random

## characters
upper = "ABCDEFGHIJKLMNOPQRSTUVWXYZ"
lower = "abcdefghijklmnopqrstuvwxyz"
numbers = "0123456789"
special_characters = "[]{}()*;_-/!&%?."

chars = upper + lower + numbers + special_characters

length = 16

password = "".join(random.sample(chars,length))

print(password)
